'''
    # Strings things
    # Python can work with string easily
'''

my_string = "I'm The Flash"
print(my_string)
# With [i] we can access to specific character in string
print(my_string[2]) # Third position - m
print(my_string[-1]) # Last character - h
print(my_string[0:5]) # From position 0 (included) to 5(excluded) - I'm T
print(my_string[:2]) # From beginning to position 2 - I'
print(my_string[4:]) # From position 4
print(my_string[::-1]) # Return reverse statement
